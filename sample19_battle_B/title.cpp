// Titleの更新処理
void UpdateTitle(void)
{
	// タイトルアニメーションのカウンターを進める
	titleCnt = (titleCnt + 1) % ANIM_MAX;	// ０１２３４０１２３４０１２３４

	// 処理を行う前にキー入力があったかをチェックする
	if (_kbhit() == 0)
	{	// キー入力がなければ何もしない
		system("cls");	// 画面クリア
		return;
	}

	// キー入力処理
	int key;
	rewind(stdin);
	key = _getch();
	if ((key == 0) || (key == 0xe0))
	{
		key = _getch();
	}

	system("cls");	// 画面クリア

	//mode = 1;
	SetMode(1);		// フィールドへシーン遷移する

}


// Titleの描画処理
